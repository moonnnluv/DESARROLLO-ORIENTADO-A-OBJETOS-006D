    private void btnModificar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificar1ActionPerformed
    int filaSeleccionada = tableRepresentantes.getSelectedRow();
    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione un representante de la tabla.");
        return;
    }

    String rut = tableRepresentantes.getValueAt(filaSeleccionada, 1).toString();
    RepresentanteDAO dao = new RepresentanteDAO();
    Representante representante = dao.buscarPorRut(rut); // MÃ©todo en RepresentanteDAO
    if (representante == null) {
        JOptionPane.showMessageDialog(this, "Representante no encontrado.");
        return;
    }

    String nuevoNombre = JOptionPane.showInputDialog("Ingrese nuevo nombre:", representante.getNombre());
    String nuevaNacionalidad = JOptionPane.showInputDialog("Ingrese nueva nacionalidad (Chilena/Extranjera):", representante.getNacionalidad());

    representante.setNombre(nuevoNombre != null ? nuevoNombre : representante.getNombre());
    representante.setNacionalidad(nuevaNacionalidad != null ? nuevaNacionalidad : representante.getNacionalidad());

    String resultado = dao.modificarRepresentante(representante);
    JOptionPane.showMessageDialog(this, resultado);

    actualizarTablaRepresentantes();
    }//GEN-LAST:event_btnModificar1ActionPerformed

