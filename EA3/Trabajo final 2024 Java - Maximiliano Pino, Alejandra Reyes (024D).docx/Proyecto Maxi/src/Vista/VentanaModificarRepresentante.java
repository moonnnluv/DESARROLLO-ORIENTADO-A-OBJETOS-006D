    private void actualizarTablaRepresentantes() {
    RepresentanteDAO dao = new RepresentanteDAO();
    DefaultTableModel model = (DefaultTableModel) tableRepresentantes.getModel();
    model.setRowCount(0);
    dao.listarRepresentantes().forEach(rep -> {
        model.addRow(new Object[] {
            rep.getNombre(),
            rep.getRut(),
            rep.getNacionalidad()
        });
    });
}


