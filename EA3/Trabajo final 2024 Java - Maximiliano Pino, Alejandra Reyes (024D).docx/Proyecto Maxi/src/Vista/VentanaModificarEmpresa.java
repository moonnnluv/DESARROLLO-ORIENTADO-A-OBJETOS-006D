private void actualizarTablaEmpresas() {
    EmpresaDAO dao = new EmpresaDAO();
    DefaultTableModel model = (DefaultTableModel) tableEmpresas.getModel();
    model.setRowCount(0);
    dao.listarEmpresas().forEach(emp -> {
        model.addRow(new Object[] {
            emp.getNombre(),
            emp.getRut(),
            emp.getGiro(),
            emp.getComuna(),
            emp.getRepresentante().getRut()
        });
    });
}

