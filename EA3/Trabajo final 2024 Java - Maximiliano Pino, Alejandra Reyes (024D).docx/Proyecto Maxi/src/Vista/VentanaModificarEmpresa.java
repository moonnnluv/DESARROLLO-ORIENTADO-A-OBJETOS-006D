    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
    int filaSeleccionada = tableEmpresas.getSelectedRow();
    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione una empresa de la tabla.");
        return;
    }

    String nombre = tableEmpresas.getValueAt(filaSeleccionada, 0).toString();
    String rut = tableEmpresas.getValueAt(filaSeleccionada, 1).toString();
    String giro = tableEmpresas.getValueAt(filaSeleccionada, 2).toString();
    String comuna = tableEmpresas.getValueAt(filaSeleccionada, 3).toString();
    String representanteRut = tableEmpresas.getValueAt(filaSeleccionada, 4).toString();

    EmpresaDAO dao = new EmpresaDAO();
    RepresentanteDAO representanteDAO = new RepresentanteDAO();

    Representante representante = representanteDAO.buscarPorRut(representanteRut);
    if (representante == null) {
        JOptionPane.showMessageDialog(this, "Representante no encontrado.");
        return;
    }

    Empresa empresa = new Empresa(rut, nombre, giro, comuna, true, 0, 0, representante);
    String nuevoNombre = JOptionPane.showInputDialog("Ingrese nuevo nombre:", nombre);
    String nuevoGiro = JOptionPane.showInputDialog("Ingrese nuevo giro:", giro);
    String nuevaComuna = JOptionPane.showInputDialog("Ingrese nueva comuna:", comuna);

    empresa.setNombre(nuevoNombre != null ? nuevoNombre : nombre);
    empresa.setGiro(nuevoGiro != null ? nuevoGiro : giro);
    empresa.setComuna(nuevaComuna != null ? nuevaComuna : comuna);

    String resultado = dao.modificarEmpresa(empresa);
    JOptionPane.showMessageDialog(this, resultado);

    actualizarTablaEmpresas();
    }//GEN-LAST:event_btnModificarActionPerformed


