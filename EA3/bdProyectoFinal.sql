START TRANSACTION;
SET time_zone = "+00:00";

-- CREAR TABLAS

CREATE TABLE `Empresa` (
  `id_empresa` INT AUTO_INCREMENT PRIMARY KEY,
  `razon_social` VARCHAR(255) NOT NULL,
  `rut_empresa` VARCHAR(20) NOT NULL UNIQUE,
  `mes_registro` INT NOT NULL,  -- Mes de registro (01-12)
  `anio_registro` INT NOT NULL,  -- Año de registro (20XX)
  `giro` VARCHAR(255),
  `id_representante` INT,  -- Clave foránea hacia la tabla `representante`
  `fecha_registro` DATETIME DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (`id_representante`) REFERENCES `representante`(`id_representante`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;


CREATE TABLE `Representante` (
  `id_representante` INT AUTO_INCREMENT PRIMARY KEY,
  `rut_representante` VARCHAR(20) NOT NULL UNIQUE,
  `nombre_representante` VARCHAR(100) NOT NULL,
  `nacionalidad` VARCHAR(50) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;





